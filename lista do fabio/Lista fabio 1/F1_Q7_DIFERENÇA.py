# Entrada
n1 = int(input('Digite o número 1:'))
n2 = int(input('Digite o número 2:'))
n3 = int(input('Digite o número 3:'))

# Processamento
dois_primeiros = n1 + n2
diferença = n2 - n3

# Saída
print(f'A soma dos dois primeiros é: {dois_primeiros}')
print(f'A diferença dos dois últimos é: {diferença}')
